    private void btnDiscoverActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDiscoverActionPerformed
        // TODO add your handling code here:
        String layerGType = null;
        try {
            layerGType = 
                Queries.getLayerGeometryType(this.conn,
                                                 this.schema,
                                                 this.object,
                                                 this.column,
                                                 this.sldrPercentage.getValue(),
                                                 0);
        } catch (Exception e) {
            LOGGER.warn("DiscoverGeometryType: " + e.toString());
        }
        btnReturn.setEnabled(true);
        if ( layerGType == null ) {
            return;
        }
        this.lblGeometryTypeValue.setText(layerGType);
    }//GEN-LAST:event_btnDiscoverActionPerformed

